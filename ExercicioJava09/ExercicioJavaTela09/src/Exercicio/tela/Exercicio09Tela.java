    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        float a = Float.parseFloat(txtA.getText());
        float b = Float.parseFloat(txtB.getText());
        float c = Float.parseFloat(txtC.getText());
        
        float areaQuadrado = (float) Math.pow(a, 2);
        float areaTriangulo = ((a * b) / 2);
        float areaTrapezio = (((a + b) / 2) * c);
        
        lblQuadrado.setText(Float.toString(areaQuadrado));
        lblTriangulo.setText(Float.toString(areaTriangulo));
        lblTrapezio.setText(Float.toString(areaTrapezio));
    }//GEN-LAST:event_btnCalcularActionPerformed

